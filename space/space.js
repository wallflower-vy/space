function showsidenav(){
    document.getElementById('nav-item-box').style.display = "block";
    document.getElementById('show-menu').style.display = "none";
    document.getElementById('close').style.display = "block";
   
}
function hideidenav(){
    document.getElementById('nav-item-box').style.display = "none";
    document.getElementById('close').style.display = "none";
    document.getElementById('show-menu').style.display = "block";
}

 
